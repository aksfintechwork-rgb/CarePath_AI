export function translateMedicalTerm(text: string | null | undefined, language: string): string {
  if (!text) return "—";
  if (!language || language === "English") return text;

  const langMap = translations[language];
  if (!langMap) return text;

  const exactMatch = langMap[text];
  if (exactMatch) return exactMatch;

  const lowerText = text.toLowerCase().trim();

  for (const [key, val] of Object.entries(langMap)) {
    if (key.toLowerCase() === lowerText) return val;
  }

  const freqAlias = frequencyAliases[lowerText];
  if (freqAlias && langMap[freqAlias]) return langMap[freqAlias];

  const timingAlias = timingAliases[lowerText];
  if (timingAlias && langMap[timingAlias]) return langMap[timingAlias];

  const durationMatch = text.match(/^(\d+)\s*(days?|weeks?|months?)$/i);
  if (durationMatch) {
    const num = durationMatch[1];
    const unit = durationMatch[2].toLowerCase();
    const translatedUnit = langMap[unit];
    if (translatedUnit) return `${num} ${translatedUnit}`;
  }

  const wordNumbers: Record<string, string> = {
    one: "1", two: "2", three: "3", four: "4", five: "5",
    six: "6", seven: "7", eight: "8", nine: "9", ten: "10",
    eleven: "11", twelve: "12", thirteen: "13", fourteen: "14", fifteen: "15",
    twenty: "20", thirty: "30",
  };
  const wordDurationMatch = text.match(/^(\w+)\s*(days?|weeks?|months?)$/i);
  if (wordDurationMatch) {
    const wordNum = wordDurationMatch[1].toLowerCase();
    const unit = wordDurationMatch[2].toLowerCase();
    const num = wordNumbers[wordNum];
    if (num) {
      const translatedUnit = langMap[unit];
      if (translatedUnit) return `${num} ${translatedUnit}`;
    }
  }

  return text;
}

export async function translateFreeTextBatch(
  texts: string[],
  language: string
): Promise<Record<string, string>> {
  const result: Record<string, string> = {};
  if (!language || language === "English") {
    texts.forEach(t => { result[t] = t; });
    return result;
  }

  const untranslated: string[] = [];
  texts.forEach(t => {
    const translated = translateMedicalTerm(t, language);
    if (translated !== t) {
      result[t] = translated;
    } else if (t && t !== "—") {
      untranslated.push(t);
    } else {
      result[t] = t;
    }
  });

  if (untranslated.length === 0) return result;

  try {
    const openai = createOpenAI();
    const joined = untranslated.map((t, i) => `${i + 1}. ${t}`).join("\n");
    const resp = await openai.chat.completions.create({
      model: "gpt-4o-mini",
      temperature: 0,
      messages: [
        {
          role: "system",
          content: `You are a medical translator. Translate the following medical/clinical phrases from English to ${language}. Return ONLY a JSON object mapping each original English phrase to its ${language} translation. Do not add explanations.`,
        },
        {
          role: "user",
          content: joined,
        },
      ],
    });
    const raw = resp.choices[0]?.message?.content?.trim() || "{}";
    const cleaned = raw.replace(/```json\s*/g, "").replace(/```/g, "").trim();
    const parsed = JSON.parse(cleaned) as Record<string, string>;
    untranslated.forEach(t => {
      result[t] = parsed[t] || t;
    });
  } catch (e) {
    untranslated.forEach(t => { result[t] = t; });
  }

  return result;
}
